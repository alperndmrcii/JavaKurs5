package Gun08;

public class _05_JavaAssignmentOperators {
    public static void main(String[] args) {

        int a=10;
        a=a+5; // a ? = 15 olur
        a+=5; // yine aynı şey 15 olur bu yazım üsttekinin kısa yazılışıdır.
        // a nın artış miktarı 5 dir.
        int b=10;
        // b=b-5; b kaç oldu 5 oldu
        b-=5; // aynısı olur yine cevap 5
        // b nin azalış miktarı 5 demek.
        int c=10;
        //c=c/5; //c 2 olur
        // c nin bölüm değeri 5 olur.
        int d=10;
        //d=d*5; 50
        d*=5;
        System.out.println("d = " +d);

























    }
}
