package Gun07;

public class _04_StringLastIndexOf {
    public static void main(String[] args) {
        // indexof gibi çalışır ama indexof aramaya baştan başlar
        // lastindexof ise aramaya sondan başlar.
        String s1="Süleyman";
        System.out.println("s1.indexof = " + s1.indexOf("n"));
        System.out.println("s1lastindexof = " + s1.lastIndexOf("n"));
        // index her zaman tek sadece arama yönü değişir.


    }
}
