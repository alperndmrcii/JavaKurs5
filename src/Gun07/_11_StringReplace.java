package Gun07;

public class _11_StringReplace {
    public static void main(String[] args) {

        // Replace : Bir String içindeki karakter(ler)i verilenle değiştirir. (HEPSİNİ)
        String text="Merhaba Dünya";
        System.out.println("texy"+text);
        System.out.println("text.replace(a,e) = "+ text.replace("a","e"));
        System.out.println("text.replace(ba,be) = " + text.replace("ba","be"));
        System.out.println("text.replace(Dünya,Java) = " + text.replace("Dünya","Java"));
        System.out.println("text.tabi = " + text.replace("Dünya","Bağırmayacaktın Anton artık ağzının yerini biliyorum"));


















    }
}
