package Gun07;

public class _12_StringReplaceFirst {
    public static void main(String[] args) {

        // ReplaceFirst: Bir String içindeki karakter(ler)i verilenle değiştirir. SADECE İLKİNİ
        String text="Merhaba Dünya";
        System.out.println("text = " + text);
        System.out.println("text.replacefirst(a,e) = " + text.replaceFirst("a","e"));
        System.out.println("text.replacefirst(r,c) = " + text.replaceFirst("r","c"));
        System.out.println("text.replacefirst(Merhaba,Selam) = " + text.replaceFirst("Merhaba","Selam"));
        System.out.println("text.replacefirst(a,'') = " + text.replaceFirst("a",""));














    }
}
